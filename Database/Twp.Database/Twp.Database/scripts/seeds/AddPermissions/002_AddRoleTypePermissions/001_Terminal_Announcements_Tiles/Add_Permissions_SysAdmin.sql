-- ADD THE MENUS AVAILABLE FOR THE ROLE 'SysAdmin'
BEGIN TRY
	BEGIN TRANSACTION ADD_PERMISSIONS_ROLE_TYPE

		DECLARE 
			@SYSTEM_ADMIN_ID UNIQUEIDENTIFIER;

		DECLARE @SYSTEM_ADMIN_PERMISSIONS_TO_ADD TABLE (
			PermissionId UNIQUEIDENTIFIER NOT NULL,
			ValueToUse NVARCHAR(1) NOT NULL
		);

		---SET ROLE IDS
		SELECT @SYSTEM_ADMIN_ID = Id FROM RoleType WHERE Name = 'SysAdmin';

		-- GET ALL THE MISING PERMISSIONS FOR THE ROLE.
		INSERT INTO @SYSTEM_ADMIN_PERMISSIONS_TO_ADD (PermissionId, ValueToUse)
		SELECT 
			P.Id,
			CASE 
				WHEN P.[Key] = 'TERMINAL_TILES' THEN 'F' 
				WHEN P.[Key] = 'ADMIN_ONLY' THEN 'F' 
			END
			FROM Permission P 
			WHERE P.Id NOT IN
			(
				SELECT RTP.PermissionId FROM RoleTypePermission RTP WITH(NOLOCK)
				WHERE RTP.RoleTypeId = @SYSTEM_ADMIN_ID			
			)
			AND P.[Key] IN ('TERMINAL_TILES','ADMIN_ONLY');

		-- FOR ALL PERMISSIONS NOT CREATED, INSERT
		IF EXISTS (SELECT TOP 1 1 FROM @SYSTEM_ADMIN_PERMISSIONS_TO_ADD)
		BEGIN
			INSERT INTO RoleTypePermission (PermissionId, RoleTypeId, [Value])
			SELECT PermissionId, @SYSTEM_ADMIN_ID, ValueToUse FROM @SYSTEM_ADMIN_PERMISSIONS_TO_ADD
		END


 	COMMIT TRANSACTION ADD_PERMISSIONS_ROLE_TYPE
END TRY
BEGIN CATCH 
  IF (@@TRANCOUNT > 0)
   BEGIN
      ROLLBACK TRANSACTION ADD_PERMISSIONS_ROLE_TYPE
	  PRINT 'ERROR'
   END 
END CATCH